    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tblKH.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xóa");
            return;
        }
        if (JOptionPane.YES_OPTION != JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn xóa không ?")) {
            return;
        }
        String id = khachHangServ.getAll().get(row).getId();
        String noti = khachHangServ.delete(id);
        JOptionPane.showMessageDialog(this, noti);
        LoadTable();
        cleanForm();
    }//GEN-LAST:event_btnXoaActionPerformed

