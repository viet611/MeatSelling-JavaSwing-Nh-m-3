    private void tblKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKHMouseClicked
        int row = tblKH.getSelectedRow();
        if (row == -1) {
            return;
        }
        KhachHangViewModel khachHang = khachHangServ.getAll().get(row);
        txtID.setText(khachHang.getId());
        txtMaKH.setText(khachHang.getMaKH());
        txtHoTen.setText(khachHang.getHoTen());
        txtSDT.setText(khachHang.getSDT());
        txtDiaChi.setText(khachHang.getDiaChi());
        txtNgaySinh.setText(khachHang.getNgaySinh());
        txtMaTV.setText(khachHang.getMaTV());
        txtHangTV.setText(khachHang.getHangTV());
        txtIDThanhVien.setText(khachHang.getIDThanhVien());
        txtSoDiemTichLuy.setText(khachHang.getSoDiemTichLuy());
        if (khachHang.getGioiTinh().equals("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
    }//GEN-LAST:event_tblKHMouseClicked

