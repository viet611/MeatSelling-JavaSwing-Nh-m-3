    public void LoadTable() {
        DefaultTableModel cn = (DefaultTableModel) tblKH.getModel();
        cn.setRowCount(0);
        for (KhachHangViewModel x : khachHangServ.getAll()) {
            Object[] rowData = {
                x.getMaKH(),
                x.getHoTen(),
                x.getSDT(),
                x.getGioiTinh(),
                x.getDiaChi(),
                x.getNgaySinh(),
                x.getMaTV(),
                x.getHangTV(),
                x.getIDThanhVien(),
                x.getSoDiemTichLuy()
            };
            cn.addRow(rowData);
        }
    }

